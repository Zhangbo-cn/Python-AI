# a = [1, 2, 3, [4, 5]]
# b1 = copy.copy(a)
# a[0] = 3
# a[3].append(6)
# print(a)
# print(b1)

# 深拷贝
a = [1, 2, 3, [4, 5]]

print()
b2 = copy.deepcopy(a)
print(id(a))
a[3] = [4, 5, 6]
print(a)
print(b2)
print(id(a))
print(id(b2))

# 总结：
# 浅拷贝：内部元素为值不可变类型，深拷贝：内部元素为值可变类型
# 浅拷贝：拷贝一层 / 深拷贝(彻底和原对象脱离关系)：拷贝多层
# 针对的是值可变类型，列表 / 字典 / 集合
# 如果针对的是值不可变类型，赋值 -> 浅拷贝和深拷贝 没差别